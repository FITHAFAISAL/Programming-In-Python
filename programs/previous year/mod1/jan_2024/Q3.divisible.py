"""Write a Python program to print all numbers between 100 and 1000 whose sum
of digits is divisible by 9."""

for i in range(100,1000):
    d_sum=0
    for j in str(i):
        d_sum+=int(j)
    if d_sum % 9==0:
        print(i,end=" ")

